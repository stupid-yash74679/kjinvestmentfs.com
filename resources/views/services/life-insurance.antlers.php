                                </h2>
                                <div id="collapseOne" class="accordion-collapse collapse show" aria-labelledby="headingOne" data-bs-parent="#accordionExample">
                                    <div class="accordion-body">
                                        <p>A general rule of thumb is to have a life cover that is at least 10-15 times your annual income. However, the ideal amount depends on your age, number of dependents, outstanding loans, and future financial goals. We perform a detailed analysis to calculate a cover amount that truly secures your family's future.</p>
                                    </div>
                                </div>
                            </div>
                            <div class="accordion-item">
                                <h2 class="accordion-header" id="headingTwo">
                                    <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
                                        What is the difference between Term Insurance and an Endowment Plan?
                                    </button>
                                </h2>
                                <div id="collapseTwo" class="accordion-collapse collapse" aria-labelledby="headingTwo" data-bs-parent="#accordionExample">
                                    <div class="accordion-body">
                                        <p>A Term Plan is a pure protection plan that offers a large death benefit for a low premium; it does not have any maturity value. An Endowment Plan combines insurance with savings, offering a death benefit as well as a lump sum amount upon maturity. We can help you decide which is better suited for your goals.</p>
                                    </div>
                                </div>
                            </div>
                            <div class="accordion-item">
                                <h2 class="accordion-header" id="headingThree">
                                    <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapseThree" aria-expanded="false" aria-controls="collapseThree">
                                        Is the claim amount paid out by life insurance taxable?
                                    </button>
                                </h2>
                                <div id="collapseThree" class="accordion-collapse collapse" aria-labelledby="headingThree" data-bs-parent="#accordionExample">
                                    <div class="accordion-body">
                                       <p>No. Under Section 10(10D) of the Income Tax Act, the death benefit (the sum assured) paid out to your nominee is completely tax-free. This ensures your family receives the full intended amount without any deductions.</p>
                                    </div>
                                </div>
                            </div>
                             <div class="accordion-item">
                                <h2 class="accordion-header" id="headingFour">
                                    <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapseFour" aria-expanded="false" aria-controls="collapseFour">
                                        What happens if I stop paying my premiums?
                                    </button>
                                </h2>
                                <div id="collapseFour" class="accordion-collapse collapse" aria-labelledby="headingFour" data-bs-parent="#accordionExample">
                                    <div class="accordion-body">
                                       <p>If premiums are not paid, the policy will lapse, and the life cover will cease. Most policies have a grace period to make the payment. If it lapses, there is usually a revival period (often 2-5 years) during which you can restart the policy by paying the outstanding premiums, sometimes with a penalty. We always advise our clients to ensure timely premium payments.</p>
