                                    <button class="accordion-button" type="button" data-bs-toggle="collapse" data-bs-target="#collapseOne" aria-expanded="true" aria-controls="collapseOne">
                                        Why do I need Comprehensive Car Insurance instead of just Third-Party?
                                    </button>
                                </h2>
                                <div id="collapseOne" class="accordion-collapse collapse show" aria-labelledby="headingOne" data-bs-parent="#accordionExample">
                                    <div class="accordion-body">
                                        <p>While Third-Party insurance is mandatory and covers damages to another person or property, it does not cover any damage to your own vehicle. Comprehensive insurance protects your car against damages from accidents, theft, fire, and natural disasters, providing complete financial security and peace of mind.</p>
                                    </div>
                                </div>
                            </div>
                            <div class="accordion-item">
                                <h2 class="accordion-header" id="headingTwo">
                                    <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
                                        What is typically covered under Home Insurance?
                                    </button>
                                </h2>
                                <div id="collapseTwo" class="accordion-collapse collapse" aria-labelledby="headingTwo" data-bs-parent="#accordionExample">
                                    <div class="accordion-body">
                                        <p>A standard home insurance policy covers the structure of your house against risks like fire, flood, earthquake, and lightning. You can also add coverage for the contents inside your home, such as furniture, appliances, and electronics, against risks like theft and damage.</p>
                                    </div>
                                </div>
                            </div>
                            <div class="accordion-item">
                                <h2 class="accordion-header" id="headingThree">
                                    <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapseThree" aria-expanded="false" aria-controls="collapseThree">
                                        How does Insured Declared Value (IDV) work for car insurance?
                                    </button>
                                </h2>
                                <div id="collapseThree" class="accordion-collapse collapse" aria-labelledby="headingThree" data-bs-parent="#accordionExample">
                                    <div class="accordion-body">
                                       <p>The Insured Declared Value (IDV) is the maximum amount your insurer will pay in case of total loss or theft of your vehicle. It is roughly equivalent to the current market value of your car. The premium you pay is calculated as a percentage of the IDV.</p>
                                    </div>
                                </div>
                            </div>
