                                        Can I transfer my No Claim Bonus (NCB) from my old car?
                                    </button>
                                </h2>
                                <div id="collapseFour" class="accordion-collapse collapse" aria-labelledby="headingFour" data-bs-parent="#accordionExample">
                                    <div class="accordion-body">
                                       <p>Yes, your No Claim Bonus (NCB) is a reward for claim-free years and belongs to you, not your car. You can transfer your accumulated NCB to a new vehicle's policy, which can significantly reduce your premium. We can assist you with the transfer process.</p>
