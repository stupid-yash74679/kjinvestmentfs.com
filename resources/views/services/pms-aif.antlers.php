                                        How is PMS different from a Mutual Fund?
                                    </button>
                                </h2>
                                <div id="collapseTwo" class="accordion-collapse collapse" aria-labelledby="headingTwo" data-bs-parent="#accordionExample">
                                    <div class="accordion-body">
                                        <p>The key differences are ownership, portfolio size, and customization. In PMS, you directly own the stocks in your demat account, the portfolio is more concentrated (fewer stocks), and the strategy can be more personalized. Mutual funds pool money from many investors, hold a more diversified portfolio, and you own units of the fund, not the stocks directly.</p>
